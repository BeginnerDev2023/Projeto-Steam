    private void BottonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottonInserirActionPerformed
        if(!TFNome.getText().equals("")) {   
        j.setNome(TFNome.getText());
        j.setAno(Integer.parseInt(TFAno.getText()));
        j.setDesenvolvedor(TFDev.getText());
        j.setGenero(TFGenero.getText());
        
        JogoDAO jDAO = new JogoDAO();
        jDAO.insert(j);
        }else{
            JOptionPane.showMessageDialog(null, "Informe o nome do jogo!");
        }
            
    }//GEN-LAST:event_BottonInserirActionPerformed

