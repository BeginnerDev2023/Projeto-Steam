    private void BottonCarregarImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottonCarregarImagemActionPerformed
        JFileChooser fc = new JFileChooser();
        int returnValue = fc.showOpenDialog(null);
        
        if(returnValue == JFileChooser.APPROVE_OPTION){
            File selectedFile = fc.getSelectedFile();
            
            try{
                j.setImagem(Files.readAllBytes(selectedFile.toPath()));
                JOptionPane.showMessageDialog(null,"Arquivo carregado com sucesso!");
            }catch(Exception e){
                 JOptionPane.showMessageDialog(null,"Erro ao carregar arquivo!");
            }
        }
    }//GEN-LAST:event_BottonCarregarImagemActionPerformed

